# Tipos int e float 
# int -> Números inteiro 
# O tipos int representa qualquer número
# Positivo e negativo. int sem sinal é considerado positivo.
print(11) #int
print(-11) #int
print(0)

# float -> Número com ponto flutuante 
# O tipos float representa qualquer número
# Positivo ou negativo com ponto flutuante.
# float sem sinal é considerado positivo.
print(1.1, 10.2)
print(0.0, -1.5)

# A função type mostra o tipo que o python inferiu o valor.
print(type(10))
print(type(22.22))
print(type('Gustavo Lima dos Santos'))

