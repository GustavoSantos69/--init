# Tipos de dados bool (bolean)
# Ao questionar algo em um programa,
# só existem duas respostas possíveis:
# sim (TRUE) ou não (FALSE).
# Existem vários operadores para "questionar".
# Dentre eles o ==, que é um operador lógico
# que questiona se um valor é igual a outro.

print(10 == 10) #sim -> Verdadeiro(True) 
print(10 == 11) #nao -> Falso(False)
print(type(True)) #True
print(type(False)) #False
print(type( 1 >= 1))
print(type( 1 == 50))
